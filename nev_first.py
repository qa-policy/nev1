numA = input("Enter the first number: ")
numB = input("Enter the second number: ")
num_sum = int(numA) + int(numB)
num_div =int(numA) / int(numB)
num_mult = int(numA) * int(numB)
num_sub =int(numA) - int(numB)
print("%s + %s = %s" % (numA, numB, str(num_sum)))
print("%s / %s = %s" % (numA, numB, str(num_div)))
print("%s * %s = %s" % (numA, numB, str(num_mult)))
print("%s - %s = %s" % (numA, numB, str(num_sub)))
